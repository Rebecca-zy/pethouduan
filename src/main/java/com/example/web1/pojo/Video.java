package com.example.web1.pojo;

public class Video {
    private int spid;
    private String sp;
    private String fm;
    private int yhid;
    private int jlid;
    private int sc;

    public int getSpid() {
        return spid;
    }

    public void setSpid(int spid) {
        this.spid = spid;
    }

    public String getSp() {
        return sp;
    }

    public void setSp(String sp) {
        this.sp = sp;
    }

    public String getFm() {
        return fm;
    }

    public void setFm(String fm) {
        this.fm = fm;
    }

    public int getYhid() {
        return yhid;
    }

    public void setYhid(int yhid) {
        this.yhid = yhid;
    }

    public int getJlid() {
        return jlid;
    }

    public void setJlid(int jlid) {
        this.jlid = jlid;
    }

    public int getSc() {
        return sc;
    }

    public void setSc(int sc) {
        this.sc = sc;
    }
}
